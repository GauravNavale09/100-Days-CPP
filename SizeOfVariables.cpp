#include<iostream>
using namespace std;
 
int main()
{
    int intType;
    float floatType;
    char charType;
    double doubleType;

    cout<<" Size of int: "<<sizeof(intType)<<" bytes"<<endl;
    cout<<" Size of float: "<<sizeof(floatType)<<" bytes"<<endl;
    cout<<" Size of char: "<<sizeof(charType)<<" bytes"<<endl;
    cout<<" Size of double: "<<sizeof(doubleType)<<" bytes"<<endl;

    return 0;

}